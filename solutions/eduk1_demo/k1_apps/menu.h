typedef enum {
    MENU_TASK_IDLE,
    MENU_TASK_RUNNING,
    MENU_TASK_INITING,
    MENU_TASK_UNINITING
} MENU_TASK_STATE_ENU;

typedef struct {
    int (*pMenuTaskInit)(void);
    int (*pMenuTaskEnd)(void);
    // user don't need to care
    MENU_TASK_STATE_ENU menu_task_state;
} MENU_TASK_TYP;

typedef enum {
    MENU_COVER_NONE,
    MENU_COVER_TEXT,
    MENU_COVER_IMG,
    MENU_COVER_FUNC,
} MENU_COVER_MODE_ENU;

typedef struct {
    MENU_COVER_MODE_ENU MenuCoverMode;
    char *text;
    icon_t *img;
    void (*draw_func)(int *);
    int draw_index;
} MENU_COVER_TYP;

typedef struct {
    struct MenuTyp **pMenuList;
    uint8_t          MenuListSize;
} MENU_LIST_TYP;

typedef struct MenuTyp {
    char *MenuName;                  // 菜单名称字符串
    MENU_COVER_TYP *MenuCover;                 // 封面
    MENU_TASK_TYP *pMenuTask;                  // 指向菜单任务的指针
    void (*pTaskKeyDeal)(key_code_t key_code); // 指向菜单任务按键处理函数的指针
    struct MENU_LIST_TYP *pChildrenList; // 指向子菜单列表的指针

    // user don't need to care
    MENU_ID_TYP     MenuID;
    struct MenuTyp *pParent; // 指向上层菜单的指针
    struct MenuTyp *pChild; // 指向子菜单的指针
    struct MenuTyp *pRight; // 指向右菜单的指针
    struct MenuTyp *pLeft;  // 指向左菜单的指针
} MENU_TYP;

